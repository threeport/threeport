package scaffold

import (
	"errors"
	"fmt"
	"os"
	"path/filepath"

	. "github.com/dave/jennifer/jen"
)

// VersionPath returns the path to the version text file.
func VersionPath() string {
	return filepath.Join("internal", "version")
}

// VersionFile returns the filename of the text file that contains the current
// version for each released component of the project.
func VersionFile() string {
	return "version.txt"
}

// CreateVersionPackage creates the version package that contains the referenced
// version of each component.
func CreateVersionPackage() error {
	f := NewFile("version")
	f.HeaderComment("generated by 'threeport-sdk create' - do not edit")
	f.Anon("embed")

	f.Comment("Version is a constant variable containing the version")
	f.Comment("//go:embed version.txt")
	f.Var().Id("Version").String()

	f.Comment("GetVersion Returns REST API Version")
	f.Func().Id("GetVersion").Params().String().Block(
		Return(Qual("strings", "TrimSuffix").Call(Id("Version"), Lit("\n"))),
	)

	versionPath := VersionPath()
	if _, err := os.Stat(versionPath); errors.Is(err, os.ErrNotExist) {
		if err := os.MkdirAll(versionPath, 0755); err != nil {
			return fmt.Errorf("could not create directores for internal version package: %s, %w", versionPath, err)
		}
	}

	// write code to file
	routesFilepath := filepath.Join(versionPath, "versions_gen.go")
	file, err := os.OpenFile(routesFilepath, os.O_CREATE|os.O_WRONLY|os.O_TRUNC, 0644)
	if err != nil {
		return fmt.Errorf("failed to open file to write generated code for version package: %w", err)
	}
	defer file.Close()
	if err := f.Render(file); err != nil {
		return fmt.Errorf("failed to render generated source code for version package: %w", err)
	}

	// create version text file if it doesn't already exist
	verstionTextFilePath := filepath.Join(versionPath, VersionFile())
	if _, err := os.Stat(verstionTextFilePath); errors.Is(err, os.ErrNotExist) {
		if err := os.WriteFile(verstionTextFilePath, []byte("v0.0.1-dev"), 0644); err != nil {
			return fmt.Errorf("failed to write version text file for version package: %w", err)
		}
	}

	return nil
}
