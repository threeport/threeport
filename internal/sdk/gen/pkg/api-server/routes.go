package apiserver

import (
	"fmt"
	"path/filepath"

	. "github.com/dave/jennifer/jen"
	"github.com/gertd/go-pluralize"
	"github.com/iancoleman/strcase"

	"github.com/threeport/threeport/internal/sdk/gen"
	"github.com/threeport/threeport/internal/sdk/util"
	cli "github.com/threeport/threeport/pkg/cli/v0"
)

// GenRoutes generates the functions that return all API server routes for each
// object.
func GenRoutes(gen *gen.Generator) error {
	for _, objCollection := range gen.VersionedApiObjectCollections {
		for _, objGroup := range objCollection.VersionedApiObjectGroups {
			pluralize := pluralize.NewClient()
			f := NewFile("routes")
			f.HeaderComment("generated by 'threeport-sdk gen' - do not edit")

			f.ImportAlias("github.com/labstack/echo/v4", "echo")

			for _, apiObj := range objGroup.ApiObjects {
				routeFuncName := fmt.Sprintf("%sRoutes", apiObj.TypeName)
				//apiObj.GetVersionHandlerName = fmt.Sprintf("Get%sVersions", apiObj.TypeName)
				//apiObj.AddHandlerName = fmt.Sprintf("Add%s", apiObj.TypeName)
				//apiObj.AddMiddlewareFuncName = fmt.Sprintf("Add%sMiddleware", apiObj.TypeName)
				//apiObj.GetAllHandlerName = fmt.Sprintf("Get%s", pluralize.Pluralize(apiObj.TypeName, 2, false))
				//apiObj.GetOneHandlerName = fmt.Sprintf("Get%s", apiObj.TypeName)
				//apiObj.GetMiddlewareFuncName = fmt.Sprintf("Get%sMiddleware", apiObj.TypeName)
				//apiObj.PatchHandlerName = fmt.Sprintf("Update%s", apiObj.TypeName)
				//apiObj.PatchMiddlewareFuncName = fmt.Sprintf("Patch%sMiddleware", apiObj.TypeName)
				//apiObj.PutHandlerName = fmt.Sprintf("Replace%s", apiObj.TypeName)
				//apiObj.PutMiddlewareFuncName = fmt.Sprintf("Put%sMiddleware", apiObj.TypeName)
				//apiObj.DeleteHandlerName = fmt.Sprintf("Delete%s", apiObj.TypeName)
				//apiObj.DeleteMiddlewareFuncName = fmt.Sprintf("Delete%sMiddleware", apiObj.TypeName)
				//gen.VersionedApiObjectCollections[i].VersionedApiObjectGroups[j].ApiObjects[k] = apiObj

				addMiddleware := Null()
				getMiddleware := Null()
				patchMiddleware := Null()
				putMiddleware := Null()
				deleteMiddleware := Null()

				if apiObj.AllowCustomMiddleware {
					addMiddleware = Id("h").Dot(apiObj.AddMiddlewareFuncName).Call().Op("...")
					getMiddleware = Id("h").Dot(apiObj.GetMiddlewareFuncName).Call().Op("...")
					patchMiddleware = Id("h").Dot(apiObj.PatchMiddlewareFuncName).Call().Op("...")
					putMiddleware = Id("h").Dot(apiObj.PutMiddlewareFuncName).Call().Op("...")
					deleteMiddleware = Id("h").Dot(apiObj.DeleteMiddlewareFuncName).Call().Op("...")
				}

				f.Comment(fmt.Sprintf(
					"%s sets up all routes for the %s handlers.", routeFuncName, apiObj.TypeName,
				))
				f.Func().Id(routeFuncName).Params(
					Id("e").Op("*").Qual(
						"github.com/labstack/echo/v4",
						"Echo",
					),
					Id("h").Op("*").Qual(
						fmt.Sprintf("%s/pkg/api-server/%s/handlers", gen.ModulePath, objCollection.Version),
						"Handler",
					),
				).Block(
					Id("e").Dot("GET").Call(
						Lit(
							fmt.Sprintf("/%s/versions", pluralize.Pluralize(strcase.ToKebab(apiObj.TypeName), 2, false)),
						),
						Id("h").Dot(apiObj.GetVersionHandlerName),
					),
					Line(),
					Id("e").Dot("POST").Call(
						Qual(
							fmt.Sprintf(
								"%s/pkg/api/%s",
								gen.ModulePath,
								objCollection.Version,
							),
							fmt.Sprintf("Path%s", pluralize.Pluralize(apiObj.TypeName, 2, false)),
						),
						Id("h").Dot(apiObj.AddHandlerName),
						addMiddleware,
					),
					Id("e").Dot("GET").Call(
						Qual(
							fmt.Sprintf(
								"%s/pkg/api/%s",
								gen.ModulePath,
								objCollection.Version,
							),
							fmt.Sprintf("Path%s", pluralize.Pluralize(apiObj.TypeName, 2, false)),
						),
						Id("h").Dot(apiObj.GetAllHandlerName),
						getMiddleware,
					),
					Id("e").Dot("GET").Call(
						Qual(
							fmt.Sprintf(
								"%s/pkg/api/%s",
								gen.ModulePath,
								objCollection.Version,
							),
							fmt.Sprintf("Path%s", pluralize.Pluralize(apiObj.TypeName, 2, false)),
						).Op("+").Lit("/:id"),
						Id("h").Dot(apiObj.GetOneHandlerName),
						getMiddleware,
					),
					Id("e").Dot("PATCH").Call(
						Qual(
							fmt.Sprintf(
								"%s/pkg/api/%s",
								gen.ModulePath,
								objCollection.Version,
							),
							fmt.Sprintf("Path%s", pluralize.Pluralize(apiObj.TypeName, 2, false)),
						).Op("+").Lit("/:id"),
						Id("h").Dot(apiObj.PatchHandlerName),
						patchMiddleware,
					),
					Id("e").Dot("PUT").Call(
						Qual(
							fmt.Sprintf(
								"%s/pkg/api/%s",
								gen.ModulePath,
								objCollection.Version,
							),
							fmt.Sprintf("Path%s", pluralize.Pluralize(apiObj.TypeName, 2, false)),
						).Op("+").Lit("/:id"),
						Id("h").Dot(apiObj.PutHandlerName),
						putMiddleware,
					),
					Id("e").Dot("DELETE").Call(
						Qual(
							fmt.Sprintf(
								"%s/pkg/api/%s",
								gen.ModulePath,
								objCollection.Version,
							),
							fmt.Sprintf("Path%s", pluralize.Pluralize(apiObj.TypeName, 2, false)),
						).Op("+").Lit("/:id"),
						Id("h").Dot(apiObj.DeleteHandlerName),
						deleteMiddleware,
					),
				)
			}

			// write code to file
			genFilepath := filepath.Join(
				"pkg",
				"api-server",
				objCollection.Version,
				"routes",
				fmt.Sprintf("%s_gen.go", strcase.ToSnake(objGroup.Name)),
			)
			_, err := util.WriteCodeToFile(f, genFilepath, true)
			if err != nil {
				return fmt.Errorf("failed to write generated code to file %s: %w", genFilepath, err)
			}
			cli.Info(fmt.Sprintf("source code for API object routes written to %s", genFilepath))
		}
	}

	return nil
}
