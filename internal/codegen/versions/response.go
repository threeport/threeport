// ResponseObjects generates code for object type conversion used in API
// responses.
func (gvc *GlobalVersionConfig) ResponseObjects() error {
	pluralize := pluralize.NewClient()
	for _, apiVersion := range gvc.Versions {
		f := NewFile(apiVersion.VersionName)
		f.HeaderComment("generated by 'threeport-codegen api-version' - do not edit")

		objects := &Statement{}
		objectTypesByPath := &Statement{}
		objectTypes := &Statement{}
		for _, name := range apiVersion.RouteNames {
			objects.Case(Id(fmt.Sprintf("ObjectType%s", name))).Block(
				Id("objStruct").Op("=").Id(name).Values(),
			)
			objects.Line()

			objectTypesByPath.Case(Id(fmt.Sprintf("Path%s", pluralize.Pluralize(name, 2, false)))).Block(
				Return().Id(fmt.Sprintf("ObjectType%s", name)),
			)
			objectTypesByPath.Line()

			objectTypes.Case(Id(name).Op(",").Op("*").Id(name).Op(",").Index().Id(name)).Block(
				Return().Id(fmt.Sprintf("ObjectType%s", name)),
			)
			objectTypes.Line()
		}

		f.Comment("GetStructByObjectType returns and instance of an object for an object type.")
		f.Func().Id("GetStructByObjectType").Params(
			Id("objectType").Id("ObjectType"),
		).Id("Object").Block(
			Var().Id("objStruct").Id("Object"),
			Line(),
			Switch(Id("objectType")).Block(
				objects,
			),
			Line(),
			Return().Id("objStruct"),
		)
		f.Line()

		f.Comment("GetObjectTypeByPath returns the object type based on an API path.")
		f.Func().Id("GetObjectTypeByPath").Params(
			Id("path").String(),
		).Id("ObjectType").Block(
			Switch(Id("path")).Block(
				objectTypesByPath,
			),
			Line(),
			Return().Id("ObjectTypeUnknown"),
		)

		f.Comment("GetObjectType returns the object type for an instance of an object.")
		f.Func().Id("GetObjectType").Params(
			Id("v").Interface(),
		).Id("ObjectType").Block(
			Switch(Id("v").Assert(Type())).Block(
				objectTypes,
			),
			Line(),
			Return().Id("ObjectTypeUnknown"),
		)

		// write code to file
		routesFilepath := filepath.Join("..", "..", "pkg", "api", apiVersion.VersionName, "response_gen.go")
		file, err := os.OpenFile(routesFilepath, os.O_CREATE|os.O_WRONLY|os.O_TRUNC, 0644)
		if err != nil {
			return fmt.Errorf("failed to open file to write generated code for response objects: %w", err)
		}
		defer file.Close()
		if err := f.Render(file); err != nil {
			return fmt.Errorf("failed to render generated source code for response objects: %w", err)
		}
		fmt.Println("code generation complete for response objects")
	}

	return nil
}

// DeleteSwitch generates code for deleting an object
// given a string representation of its type and an ID.
func (gvc *GlobalVersionConfig) DeleteSwitch() error {
	for _, apiVersion := range gvc.Versions {
		// pluralize := pluralize.NewClient()
		f := NewFile(apiVersion.VersionName)
		f.HeaderComment("generated by 'threeport-codegen api-model' - do not edit")

		objects := &Statement{}
		for _, name := range apiVersion.RouteNames {
			objects.Case(Lit(name)).Block(
				If(Id("_").Op(",").Id("err")).Op(":=").Id(
					fmt.Sprintf("Delete%s", name),
				).Call(List(Id("apiClient"), Id("apiAddr"), Id("id"))).Op(";").Err().Op("!=").Nil().Block(
					Return().Qual(
						"fmt", "Errorf",
					).Call(Lit(fmt.Sprintf("failed to delete %s: %%w", name)).Op(",").Id("err")),
				),
			)
			objects.Line()
		}

		f.Comment("DeleteObjectByTypeAndID deletes an instance given a string representation of its type and ID.")
		f.Func().Id("DeleteObjectByTypeAndID").Params(
			Id("apiClient").Op("*").Qual("net/http", "Client"),
			Id("apiAddr").Id("string"),
			Id("objectType").Id("string"),
			Id("id").Id("uint"),
		).Id("error").Block(
			Line(),
			Switch(Id("objectType")).Block(
				objects,
			),
			Line(),
			Return().Nil(),
		)
		f.Line()

		// write code to file
		routesFilepath := filepath.Join("..", "..", "pkg", "client", apiVersion.VersionName, "delete_object.go")
		file, err := os.OpenFile(routesFilepath, os.O_CREATE|os.O_WRONLY|os.O_TRUNC, 0644)
		if err != nil {
			return fmt.Errorf("failed to open file to write generated code for response objects: %w", err)
		}
		defer file.Close()
		if err := f.Render(file); err != nil {
			return fmt.Errorf("failed to render generated source code for response objects: %w", err)
		}
		fmt.Println("code generation complete for response objects")

	}

	return nil
}
